package week3.d_221117.Mission2_2;

public class Subject {
    private String subjectName;
    private int score;

    Subject(String subjectName, int score) {
        this.subjectName = subjectName;
        this.score = score;
    }

    public String getSubjectName() {
        return subjectName;
    }

    public int getScore() {
        return score;
    }
}
